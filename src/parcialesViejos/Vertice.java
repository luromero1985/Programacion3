package parcialesViejos;

public class Vertice{
    // Clase para representar un vértice y su color
  
	  private int id;
	    private String color;

	    public Vertice(int id, String color) {
	        this.id = id;
	        this.color = color;
	    }

	    public int getId() {
	        return id;
	    }

	    public String getColor() {
	        return color;
	    }
        
}
